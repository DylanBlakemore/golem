func TestHTTPServerEndToEnd(t *testing.T) {
	dir := t.TempDir()
	origDir := chDir(t, dir)
	defer restoreDir(t, origDir)

	writeFile(t, filepath.Join(dir, "server.golem"), `import "net/http"
import "fmt"

pub fn main() do
  http.handleFunc("/", fn(w: http.ResponseWriter, r: *http.Request) do
    fmt.fprintln(w, "Hello from Golem!")
  end)
  fmt.println("Listening on :8080")
  http.listenAndServe(":8080", nil)
end`)

	mkDir(t, filepath.Join(dir, "build"))

	ok := compileFile("server.golem", false)
	if !ok {
		t.Fatal("HTTP server example failed to compile")
	}

	outPath := filepath.Join(dir, "build", "server.golem.go")
	data, err := os.ReadFile(outPath)
	if err != nil {
		t.Fatalf("could not read output: %v", err)
	}
	out := string(data)

	// Verify generated code is readable and idiomatic.
	assertContains(t, out, "package main")
	assertContains(t, out, "// Code generated by golem. DO NOT EDIT.")
	assertContains(t, out, `"net/http"`)
	assertContains(t, out, `"fmt"`)
	assertContains(t, out, "func main()")
	assertContains(t, out, "http.HandleFunc")
	assertContains(t, out, "func(w http.ResponseWriter, r *http.Request)")
	assertContains(t, out, `fmt.Fprintln(w, "Hello from Golem!")`)
	assertContains(t, out, `fmt.Println("Listening on :8080")`)
	assertContains(t, out, `http.ListenAndServe(":8080", nil)`)

	// Verify the generated code compiles as valid Go.
	goMod := filepath.Join(dir, "build", "go.mod")
	writeFile(t, goMod, "module golem-test\n\ngo 1.21\n")
	goVet := exec.Command("go", "vet", "./...")
	goVet.Dir = filepath.Join(dir, "build")
	if vetOut, err := goVet.CombinedOutput(); err != nil {
		t.Fatalf("go vet failed on generated code: %v\n%s", err, vetOut)
	}
	goBuild := exec.Command("go", "build", "-o", os.DevNull, ".")
	goBuild.Dir = filepath.Join(dir, "build")
	if buildOut, err := goBuild.CombinedOutput(); err != nil {
		t.Fatalf("go build failed on generated code: %v\n%s", err, buildOut)
	}
}

func assertContains(t *testing.T, haystack, needle string) {
	t.Helper()
	if !strings.Contains(haystack, needle) {
		t.Errorf("output should contain %q", needle)
	}
}

// Helpers.

func writeFile(t *testing.T, path, content string) {
	t.Helper()
	if err := os.WriteFile(path, []byte(content), 0o600); err != nil {
		t.Fatalf("writeFile: %v", err)
	}
}

func mkDir(t *testing.T, path string) {
	t.Helper()
	if err := os.MkdirAll(path, dirPerm); err != nil {
		t.Fatalf("mkDir: %v", err)
	}
}

func chDir(t *testing.T, dir string) string {
	t.Helper()
	orig, err := os.Getwd()
	if err != nil {
		t.Fatalf("getwd: %v", err)
	}
	if err := os.Chdir(dir); err != nil {
		t.Fatalf("chdir: %v", err)
	}
	return orig
}

func restoreDir(t *testing.T, dir string) {
	t.Helper()
	if err := os.Chdir(dir); err != nil {
		t.Fatalf("restore chdir: %v", err)
	}
}
