func TestGoFormatProducesValidGo(t *testing.T) {
	// The fact that Generate() doesn't error proves the output is valid Go syntax.
	// This test exercises a more complex program.
	out := generate(t, `import "fmt"

pub type Point = { x: Float, y: Float }

pub fn main() do
  let p = Point { x: 1.0, y: 2.0 }
  fmt.println(p.x)
  if p.x > 0.0 do
    fmt.println("positive")
  end else do
    fmt.println("non-positive")
  end
end`)
	// Just verify it produced something reasonable.
	assertContains(t, out, "func main()")
	assertContains(t, out, "type Point struct")
}
