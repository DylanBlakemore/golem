func TestEmptyModule(t *testing.T) {
	// Should still produce valid Go with header and package.
	mod := &ast.Module{File: "empty.golem"}
	out, err := Generate(mod, "empty.golem")
	if err != nil {
		t.Fatalf("codegen error: %v", err)
	}
	s := string(out)
	assertContains(t, s, "package main")
	assertContains(t, s, "// Code generated by golem. DO NOT EDIT.")
}

