func TestImportSorting(t *testing.T) {
	out := generate(t, `import "net/http"
import "fmt"

pub fn main() do
  fmt.println("hello")
end`)
	fmtIdx := strings.Index(out, `"fmt"`)
	httpIdx := strings.Index(out, `"net/http"`)
	if fmtIdx > httpIdx {
		t.Errorf("expected fmt before net/http in import block")
	}
}

